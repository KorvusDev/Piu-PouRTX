    #region Fruits
    public void SelectFruit(int fruitID)
    {
        StartCoroutine(SpawnFruit(fruitID));
    }

    IEnumerator SpawnFruit(int fruitID)
    {
        sActualFruit = 0;
        yield return new WaitForSeconds(0.1f);
        sActualFruit = fruitID;
        yield return new WaitForSeconds(0.01f);
        if (fruitID == 1) {Instantiate(Fruit1);}
        if (fruitID == 2) {Instantiate(Fruit2);}
        if (fruitID == 3) {Instantiate(Fruit3);}
        if (fruitID == 4) {Instantiate(Fruit4);}
        if (fruitID == 5) {Instantiate(Fruit5);}
        if (fruitID == 6) {Instantiate(Fruit6);}
        if (fruitID == 7) {Instantiate(Fruit7);}
        if (fruitID == 8) {Instantiate(Fruit8);}
        if (fruitID == 9) {Instantiate(Fruit9);}
    }
    #endregion
    #region Potions
    public void SelectPotion(int potionID)
    {
        StartCoroutine(SpawnPotion(potionID));
    }

    IEnumerator SpawnPotion(int potionID)
    {
        sActualFruit = 0;
        yield return new WaitForSeconds(0.1f);
        sActualFruit = potionID + 9;
        yield return new WaitForSeconds(0.01f);
        if (potionID == 1) {Instantiate(Potion1);}
        if (potionID == 2) {Instantiate(Potion2);}
        if (potionID == 3) {Instantiate(Potion3);}
        if (potionID == 4) {Instantiate(Potion4);}
        if (potionID == 5) {Instantiate(Potion5);}
        if (potionID == 6) {Instantiate(Potion6);}
        if (potionID == 7) {Instantiate(Potion7);}
        if (potionID == 8) {Instantiate(Potion8);}
    }
    #endregion
